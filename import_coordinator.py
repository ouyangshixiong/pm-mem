DEFAULT_SOURCE_SYSTEM = "外部创作系统"


@dataclass
class ExternalWorkPayload:
    """Normalized source material from an external creative system."""

    source_system: str
    external_work_id: str
    work_name: str
    source_url: str = ""
    story: str = ""
    script: str = ""
    storyboard_script: str = ""
    images: List[str] = field(default_factory=list)
    raw_payload: Dict[str, Any] = field(default_factory=dict)


@dataclass
class LayerDraft:
    """One memory layer generated by one import agent."""

    layer_id: str
    agent_name: str
    content: str
    role_id: str = ""
    role_name: str = ""
    role_file: str = ""
    role_prompt: str = ""
    role_prompt_hash: str = ""
    llm_provider: str = ""
    llm_processed: bool = False
    llm_model: str = ""
    llm_endpoint: str = ""
    llm_primary_error: str = ""
    llm_error: str = ""
    notes: List[str] = field(default_factory=list)


class ImportLayerAgent:
    """Base class for deterministic layer agents."""

    layer_id = ""
    agent_name = ""
    role_id = ""

    def build(
        self,
        payload: ExternalWorkPayload,
        drafts: Dict[str, LayerDraft],
    ) -> LayerDraft:
        raise NotImplementedError

    def _empty_hint(self, label: str) -> str:
        return f"（外部系统未提供{label}，待人工审核补充。）"

    def _section(self, title: str, content: str, empty_label: Optional[str] = None) -> str:
        body = (
            content.strip()
            if content and content.strip()
            else self._empty_hint(empty_label or title)
        )
        return f"## {title}\n\n{body}"

    def _draft(self, content: str, notes: Optional[List[str]] = None) -> LayerDraft:
        return LayerDraft(
            self.layer_id,
            self.agent_name,
            content,
            role_id=self.role_id,
            notes=notes or [],
        )

    def build_llm_prompt(
        self,
        payload: ExternalWorkPayload,
        draft: LayerDraft,
        role_prompt: str,
        previous_drafts: Dict[str, LayerDraft],
    ) -> str:
        layer_name = memory_manager.get_layer_definition(self.layer_id)["layer_name"]
        previous_summary = _summarize_previous_drafts(previous_drafts)
        return f"""# 角色 Prompt
{role_prompt or "（未配置角色 prompt，请按目标层规则处理。）"}

# 任务
你正在把外部作品导入 pm-mem Markdown 分层记忆。请以当前角色身份处理并提炼要写入「{layer_name}」的内容。

# 输出要求
- 只输出可以直接写入该记忆层的 Markdown 正文，不要输出代码块。
- 保留外部素材中的事实，不要编造未出现的人物、关系、时间线或场景。
- 不确定的信息标注为“待人工确认”。
- 归档层需要保留原文或原始结构，不要为了摘要而丢失可复查内容。
- 内容应可供后续编剧、分镜师、制片人继续读取和维护。

# 作品信息
作品名称：{payload.work_name}
来源系统：{payload.source_system}
外部作品 ID：{payload.external_work_id}
来源 URL：{payload.source_url or "未提供"}

# 已生成的前置记忆层
{previous_summary or "（暂无前置层。）"}

# 当前确定性草稿
{draft.content}

# 外部故事
{_clip(payload.story, 8000) or "（未提供）"}

# 外部剧本
{_clip(payload.script, 8000) or "（未提供）"}

# 外部分镜剧本
{_clip(payload.storyboard_script, 8000) or "（未提供）"}

# 外部图片
{f"已随请求附加 {len(payload.images)} 张图片，请结合图片内容校验视觉信息。" if payload.images else "（未提供）"}
"""


class MetadataImportAgent(ImportLayerAgent):
    layer_id = "work_metadata"
    agent_name = "作品元数据导入智能体"
    role_id = "制片人"

    def build(
        self,
        payload: ExternalWorkPayload,
        drafts: Dict[str, LayerDraft],
    ) -> LayerDraft:
        imported_at = datetime.now().strftime(memory_manager.TIME_FORMAT)
        raw_keys = (
            ", ".join(sorted(payload.raw_payload.keys()))
            if payload.raw_payload
            else "无"
        )
        content = f"""# 作品元数据

作品名称：{payload.work_name}
来源系统：{payload.source_system}
来源workid：{payload.external_work_id}
来源URL：{payload.source_url or "未提供"}
导入时间：{imported_at}
导入方式：外部项目读取原始内容后调用 pm-mem 导入协作接口
内容类型：漫画/短剧改编素材
原始字段：{raw_keys}

## 导入说明

本作品由导入协作接口生成分层记忆草稿。用户可在 Web 管理页审核、修订和锁定各层内容。
"""
        return self._draft(content)


