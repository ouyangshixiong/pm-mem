class ConsistencyReviewImportAgent(ImportLayerAgent):
    layer_id = "import_review"
    agent_name = "导入一致性校验智能体"
    role_id = "一致性校验员"

    def build(
        self,
        payload: ExternalWorkPayload,
        drafts: Dict[str, LayerDraft],
    ) -> LayerDraft:
        notes = []
        if not payload.story.strip():
            notes.append("未提供 story 字段。")
        if not payload.script.strip():
            notes.append("未提供 script 字段。")
        if not payload.storyboard_script.strip():
            notes.append("未提供 storyboard_script 字段。")
        if not notes:
            notes.append("故事、剧本、分镜剧本均已提供。")

        content = "# 导入校验\n\n" + "\n".join(f"- {note}" for note in notes)
        return self._draft(content, notes=notes)


class ExternalWorkImportCoordinator:
    """Coordinate layer agents and write their drafts to Markdown memory."""

    def __init__(
        self,
        operator: str = "导入协作智能体",
        llm_client: Optional[LocalResponsesLLMClient] = None,
        backup_llm_client: Optional[DeepSeekChatLLMClient] = None,
        llm_settings: Optional[ImportLLMSettings] = None,
    ):
        self.operator = operator
        self.llm_settings = llm_settings or load_import_llm_settings()
        self.llm_client = llm_client
        if self.llm_client is None:
            self.llm_client = LocalResponsesLLMClient(self.llm_settings)
        self.backup_llm_client = backup_llm_client
        self.layer_agents: List[ImportLayerAgent] = [
            MetadataImportAgent(),
            CoreSettingImportAgent(),
            CharacterProfileImportAgent(),
            PlotContextImportAgent(),
            ScriptArchiveImportAgent(),
            StoryboardArchiveImportAgent(),
        ]
        self.review_agent = ConsistencyReviewImportAgent()

    def import_work(
        self,
        payload: ExternalWorkPayload,
        dry_run: bool = False,
    ) -> Dict[str, Any]:
        existing_work_id = find_existing_imported_work(
            payload.source_system,
            payload.external_work_id,
        )
        work_id = existing_work_id
        created = False
        if work_id is None and not dry_run:
            work_id = memory_manager.create_work(payload.work_name)
            created = True

        drafts: Dict[str, LayerDraft] = {}
        for agent in self.layer_agents:
            draft = agent.build(payload, drafts)
            draft = self._process_draft_with_role_llm(
                agent,
                payload,
                draft,
                drafts,
            )
            drafts[draft.layer_id] = draft

        review = self.review_agent.build(payload, drafts)
        remem_tasks: Dict[str, Dict[str, Any]] = {}
        if not dry_run and work_id is not None:
            for draft in drafts.values():
                remem_tasks[draft.layer_id] = self._write_draft_via_remem(
                    work_id=work_id,
                    payload=payload,
                    draft=draft,
                )

        return {
            "success": True,
            "dry_run": dry_run,
            "created": created,
            "work_id": work_id,
            "existing_work_id": existing_work_id,
            "work_name": payload.work_name,
            "source_system": payload.source_system,
            "external_work_id": payload.external_work_id,
            "layers": {
                layer_id: {
                    "agent_name": draft.agent_name,
                    "role_id": draft.role_id,
                    "role_name": draft.role_name,
                    "role_file": draft.role_file,
                    "role_prompt": draft.role_prompt,
                    "role_prompt_hash": draft.role_prompt_hash,
                    "llm_provider": draft.llm_provider,
                    "llm_processed": draft.llm_processed,
                    "llm_model": draft.llm_model,
                    "llm_endpoint": draft.llm_endpoint,
                    "llm_primary_error": draft.llm_primary_error,
                    "llm_error": draft.llm_error,
                    "content": draft.content,
                    "notes": draft.notes,
                }
                for layer_id, draft in drafts.items()
            },
            "review": {
                "agent_name": review.agent_name,
                "content": review.content,
                "notes": review.notes,
            },
            "remem_tasks": remem_tasks,
            "web_url": f"/work/{work_id}" if work_id else None,
        }

    def _write_draft_via_remem(
        self,
        work_id: str,
        payload: ExternalWorkPayload,
        draft: LayerDraft,
    ) -> Dict[str, Any]:
        role = RoleFactory.create(draft.role_id)
        task_input = (
            f"导入外部作品《{payload.work_name}》的 {draft.layer_id} 层记忆。"
            "请检索已有记忆，合并新增事实，锁定或冲突内容应标记而不是覆盖。\n\n"
            f"{draft.content}"
        )
        context = TaskContext(
            task_type="external_work_import",
            source="external_work_import_coordinator",
            role_id=role.role_id,
            metadata={
                "work_id": work_id,
                "target_layers": [draft.layer_id],
                "source_system": payload.source_system,
                "external_work_id": payload.external_work_id,
                "source_url": payload.source_url,
                "llm_provider": draft.llm_provider,
                "llm_model": draft.llm_model,
                "layer_metadata": _draft_metadata(payload, draft),
            },
        )
        agent = ReMemAgent(
            llm=_SingleActLLM(draft.content),
            memory_store=MarkdownLayerMemoryStore(),
            trace_store=MarkdownTraceStore(),
            max_iterations=1,
            retrieval_k=6,
        )
        result = agent.run_task(task_input, role=role, context=context)
        return {
            "task_id": result.get("task_id"),
            "status": result.get("status"),
            "memory_updated": result.get("memory_updated", False),
            "applied_operations": result.get("applied_operations", []),
            "conflicts": result.get("conflicts", []),
        }

    def _process_draft_with_role_llm(
        self,
        agent: ImportLayerAgent,
        payload: ExternalWorkPayload,
        draft: LayerDraft,
        previous_drafts: Dict[str, LayerDraft],
    ) -> LayerDraft:
        role = role_manager.get_role_config(draft.role_id)
        if role:
            draft.role_name = role["role_name"]
            draft.role_file = role["role_file"]
            draft.role_prompt = role["prompt"]
            draft.role_prompt_hash = role["prompt_hash"]

        draft.llm_provider = "local_proxy_responses"
        draft.llm_model = self.llm_settings.model
        draft.llm_endpoint = self.llm_settings.endpoint
        if self.llm_client is None:
            self.llm_client = LocalResponsesLLMClient(self.llm_settings)

        prompt = agent.build_llm_prompt(payload, draft, draft.role_prompt, previous_drafts)
        prompt = _clip_middle(prompt, self.llm_settings.max_prompt_chars)
        primary_error = ""
        try:
            refined = self.llm_client.generate(prompt, images=payload.images)
            refined = _clean_llm_markdown(refined)
            if refined:
                draft.content = refined
                draft.llm_processed = True
                return draft
            primary_error = "local proxy LLM returned empty content"
        except Exception as exc:
            primary_error = _clip(str(exc), 1000)

        draft.llm_primary_error = primary_error
        backup_error = self._try_deepseek_backup(prompt, payload, draft)
        if backup_error:
            draft.llm_error = _clip(
                f"local_proxy_responses: {primary_error}\n"
                f"deepseek_backup: {backup_error}",
                1200,
            )
            if not self.llm_settings.fallback_to_deterministic:
                raise RuntimeError(draft.llm_error)
        return draft

    def _try_deepseek_backup(
        self,
        prompt: str,
        payload: ExternalWorkPayload,
        draft: LayerDraft,
    ) -> str:
        backup_settings = self.llm_settings.deepseek_backup
        if self.backup_llm_client is None:
            self.backup_llm_client = DeepSeekChatLLMClient(backup_settings)

        try:
            refined = self.backup_llm_client.generate(prompt, images=payload.images)
            refined = _clean_llm_markdown(refined)
            if not refined:
                return "DeepSeek returned empty content"
            draft.content = refined
            draft.llm_provider = "deepseek_backup"
            draft.llm_model = backup_settings.model
            draft.llm_endpoint = backup_settings.endpoint
            draft.llm_processed = True
            draft.llm_error = ""
            return ""
        except Exception as exc:
            return _clip(str(exc), 1000)


class _SingleActLLM:
    """Small deterministic adapter used to route prepared import drafts via ReMem."""

    def __init__(self, action_output: str):
        self.action_output = action_output

    def __call__(self, prompt: str) -> str:
        if "请选择下一步动作" in prompt or "请选择动作" in prompt:
            return "act"
        return f"Act: {self.action_output}"

    def get_model_info(self) -> Dict[str, Any]:
        return {"model": "deterministic-import-act", "context_length_tokens": 64000}


def build_external_work_payload(
    external_work_id: str,
    work_name: str,
    source_system: str = DEFAULT_SOURCE_SYSTEM,
    source_url: str = "",
    story: str = "",
    script: str = "",
    storyboard_script: str = "",
    images: Optional[List[str]] = None,
    raw_payload: Optional[Dict[str, Any]] = None,
) -> ExternalWorkPayload:
    return ExternalWorkPayload(
        source_system=(source_system or DEFAULT_SOURCE_SYSTEM).strip(),
        external_work_id=str(external_work_id).strip(),
        work_name=work_name.strip(),
        source_url=source_url.strip(),
        story=story or "",
        script=script or "",
        storyboard_script=storyboard_script or "",
        images=images or [],
        raw_payload=raw_payload or {},
    )


def find_existing_imported_work(source_system: str, external_work_id: str) -> Optional[str]:
    source_markers = [
        f"来源系统：{source_system}",
        f"来源系统:{source_system}",
        f"source_system: {source_system}",
    ]
    id_markers = [
        f"来源workid：{external_work_id}",
        f"来源workid:{external_work_id}",
        f"external_work_id: {external_work_id}",
    ]

    for work in memory_manager.list_works():
        work_id = work["work_id"]
        try:
            metadata = memory_manager.get_layer_content(work_id, "work_metadata")
        except Exception:
            continue
        front_matter = metadata.get("metadata", {})
        if (
            str(front_matter.get("source_system", "")).strip() == source_system
            and str(front_matter.get("external_work_id", "")).strip()
            == str(external_work_id)
        ):
            return work_id
        content = metadata.get("content", "")
        if any(marker in content for marker in source_markers) and any(
            marker in content for marker in id_markers
        ):
            return work_id
    return None


